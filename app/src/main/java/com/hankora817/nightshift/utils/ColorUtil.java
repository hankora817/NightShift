package com.hankora817.nightshift.utils;

public class ColorUtil
{
	public static String getColorValue(int position)
	{
		if (position == 0)
			return "#30ff9f80";
		else if (position == 1)
			return "#30ff9673";
		else if (position == 2)
			return "#40ff8c66";
		else if (position == 3)
			return "#40ff8359";
		else if (position == 4)
			return "#50ff794d";
		else if (position == 5)
			return "#50ff7040";
		else if (position == 6)
			return "#50ff6633";
		else if (position == 7)
			return "#50ff5c26";
		else if (position == 8)
			return "#50ff5319";
		else if (position == 9)
			return "#50ff490d";
		else if (position == 10)
			return "#50ff4000";
		else
			return "#30ff9f80";
	}
	
}
